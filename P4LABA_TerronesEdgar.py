import turtle

t = turtle.Turtle()

for i in range(4):
    t.forward(100)  # Move forward by 100 units
    t.left(90) # turns at 90 degree angle

for i in range(3):
    t.forward(100)
    t.left(120) #turns at 120 degree angle
    
turtle.done()